function renderScoreFeedback(score) {
    if (score >= 80) return `<div style="background:rgba(0,200,100,0.08); border:1px solid rgba(0,200,100,0.25); border-radius:10px; padding:14px;"><span style="color:#00c864; font-weight:700;">🏆 Excellent Answer!</span> <span style="color:var(--text-muted); font-size:0.88rem;">You demonstrated strong understanding of the key concepts.</span></div>`;
    if (score >= 60) return `<div style="background:rgba(59,130,246,0.08); border:1px solid var(--border-bright); border-radius:10px; padding:14px;"><span style="color:var(--primary); font-weight:700;">✅ Good Answer.</span> <span style="color:var(--text-muted); font-size:0.88rem;">You covered most key concepts. Review the lesson to strengthen any gaps.</span></div>`;
    return `<div style="background:rgba(255,165,0,0.08); border:1px solid rgba(255,165,0,0.3); border-radius:10px; padding:14px;"><span style="color:orange; font-weight:700;">📖 Needs Improvement.</span> <span style="color:var(--text-muted); font-size:0.88rem;">Please re-read the module and resubmit with more specific details and key terms.</span></div>`;
}

function isOverdue(deadlineStr) {
    return new Date(deadlineStr) < new Date();
}

function submitAssignment(index) {
    const textarea = document.getElementById(`assignment-text-${index}`);
    if (!textarea || textarea.value.trim().length < 30) {
        textarea.style.borderColor = '#ff3c3c';
        textarea.placeholder = '⚠️ Please write a more detailed answer before submitting (minimum 30 characters)...';
        return;
    }
    const answer = textarea.value.trim();
    const score = autoGradeAssignment(answer, assignmentsData[index].keywords);
    assignmentsData[index].submitted = true;
    assignmentsData[index].answer = answer;
    assignmentsData[index].score = score;
    renderAssignments();
}

