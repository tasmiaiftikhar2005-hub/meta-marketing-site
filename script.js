function getDeadline(daysFromNow) {
    const date = new Date();
    date.setDate(date.getDate() + daysFromNow);
    return date.toLocaleDateString('en-PK', { year: 'numeric', month: 'long', day: 'numeric' });
}

function autoGradeAssignment(answer, keywords) {
    if (!answer || answer.trim().length < 50) return 0;
    const lowerAnswer = answer.toLowerCase();
    let matchCount = 0;
    keywords.forEach(kw => {
        if (lowerAnswer.includes(kw.toLowerCase())) matchCount++;
    });
    const keywordScore = Math.round((matchCount / keywords.length) * 70);
    const lengthBonus = answer.trim().split(' ').length > 100 ? 20 : answer.trim().split(' ').length > 50 ? 10 : 0;
    const structureBonus = (answer.includes('\n') || answer.includes('.')) ? 10 : 0;
    return Math.min(100, keywordScore + lengthBonus + structureBonus);
}

